namespace BeeEngine
{
    public enum LogLevel
    {
        /// <summary>
        /// Debug log level
        /// </summary>
        Trace = 4,

        /// <summary>
        /// Information log level
        /// </summary>
        Information = 3,

        /// <summary>
        /// Warning log level
        /// </summary>
        Warning = 2,

        /// <summary>
        /// Error log level
        /// </summary>
        Error = 1,

        /// <summary>
        /// No logging
        /// </summary>
        None = 0
    }
}